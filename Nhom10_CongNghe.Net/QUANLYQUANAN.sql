CREATE TABLE THUCPHAM
(
	MaTP CHAR (10) NOT NULL,
	TenTP NVARCHAR(10),
	DONVITINH NVARCHAR(10),
	CONSTRAINT PK_THUCPHAM PRIMARY KEY(MaTP)
)

CREATE TABLE PhieuNhapThucPham
(
	MaPhieu CHAR (10) NOT NULL,
	NgayNhapPhieu DATETIME,
	TenPhieu NVARCHAR (40),
	MANV CHAR (10) NOT NULL,
	MaNCC CHAR (10) NOT NULL,
	CONSTRAINT PK_PhieuNhapThucPham PRIMARY KEY(MaPhieu),
	CONSTRAINT FK_NHANVIEN_PhieuNhapThucPham FOREIGN KEY(MANV) REFERENCES NHANVIEN(MANV),
	CONSTRAINT FK_NHACUNGCAP_PhieuNhapThucPham FOREIGN KEY(MaNCC) REFERENCES NHACUNGCAP(MaNCC)
