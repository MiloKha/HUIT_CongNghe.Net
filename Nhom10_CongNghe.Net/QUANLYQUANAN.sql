	CONSTRAINT FK_PhieuNhapThucPham_ChiTietThucPham FOREIGN KEY(MaPhieu) REFERENCES PhieuNhapThucPham(MaPhieu)
)

CREATE TABLE BANAN
(
	MABAN CHAR(10) NOT NULL,
	SOCHONGOI INT,
	TRANGTHAI NVARCHAR(30),
