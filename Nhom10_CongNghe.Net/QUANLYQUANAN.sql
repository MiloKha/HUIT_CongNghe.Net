(
	MaPYC CHAR (10) NOT NULL,
	MANV CHAR (10) NOT NULL,
	NgayYC DATETIME,
	MABAN CHAR(10) NOT NULL,
	TrangThai NVARCHAR(30),
	MaKH CHAR (10) NOT NULL,
	CONSTRAINT PK_PHIEUYEUCAU PRIMARY KEY(MaPYC),
	CONSTRAINT FK_PhieuYeuCau_BANAN FOREIGN KEY (MABAN) REFERENCES BANAN(MABAN),
	CONSTRAINT FK_PhieuYeuCau_KHACHHANG FOREIGN KEY(MaKH) REFERENCES KHACHHANG(MaKH)
)


CREATE TABLE ChiTietYeuCau
(
	MaPYC CHAR (10) NOT NULL,
	MaMA CHAR (10) NOT NULL,
	DONGIA FLOAT,
	SOLUONG INT,
	CONSTRAINT PF_ChiTietYeuCau PRIMARY KEY (MaPYC, MaMA),
	CONSTRAINT FK_PHIEUYEUCAU_ChiTietYeuCau FOREIGN KEY(MaPYC) REFERENCES PHIEUYEUCAU(MaPYC),
	CONSTRAINT FK_MONAN_ChiTietYeuCau FOREIGN KEY(MaMA) REFERENCES MONAN(MaMA)
)


CREATE TABLE HOADON
(
	MAHD CHAR (10) NOT NULL,
